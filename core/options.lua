-- =============================================================================
--  NeoVim 基础 UI 与行为设置
-- =============================================================================

local opt = vim.opt  -- 语法糖，少敲几个字

-- -----------------------------------------------------------------------------
--  行号
-- -----------------------------------------------------------------------------
opt.number         = true  -- 左侧显示绝对行号
opt.relativenumber = true  -- 同时显示相对行号（跳转、复制、删除更高效）

-- -----------------------------------------------------------------------------
--  缩进与制表符
-- -----------------------------------------------------------------------------
opt.tabstop     = 4  -- 1 个 <Tab> 字符显示宽度
opt.shiftwidth  = 4  -- >>、<< 以及自动缩进时移动的列数
opt.expandtab   = true  -- 用空格代替真正的制表符
                        --   开启：Python、Go、JS 等推荐
                        --   关闭：Makefile、Go template 等需要真 Tab
opt.autoindent  = true  -- 新行延续上一行的缩进
opt.smartindent = true  -- 遇到花括号、关键字时智能缩进（对 C 系语言友好）

-- -----------------------------------------------------------------------------
--  折行
-- -----------------------------------------------------------------------------
opt.wrap   = true  -- 超过窗口宽度时自动折到下一屏行
opt.linebreak = true -- 折行时尽量在单词边界处断开，避免砍断单词

-- -----------------------------------------------------------------------------
--  光标与滚动
-- -----------------------------------------------------------------------------
opt.cursorline = true      -- 高亮光标所在行（视觉定位）
opt.scrolloff  = 8         -- 滚屏时保持光标上下 8 行可见
opt.sidescrolloff = 8      -- 水平滚屏时保持左右 8 列可见

-- -----------------------------------------------------------------------------
--  鼠标支持
-- -----------------------------------------------------------------------------
-- 支持的 mode 列表：
--   n = Normal      v = Visual     i = Insert
--   c = Cmd-line    h = Help       a = 全部
opt.mouse:append('a')  -- 允许在所有模式下使用鼠标点击/拖拽/滚轮

-- -----------------------------------------------------------------------------
--  剪贴板
-- -----------------------------------------------------------------------------
--  "unnamed"   = * 寄存器（传统 X11 复制）
--  "unnamedplus" = + 寄存器（现代 Linux/Wayland、macOS、Windows）
opt.clipboard:append('unnamedplus') -- y/d/p 操作直接同步到系统剪贴板

-- -----------------------------------------------------------------------------
--  搜索
-- -----------------------------------------------------------------------------
opt.ignorecase = true  -- 默认搜索忽略大小写
opt.smartcase  = true  -- 若输入包含大写字母，则临时区分大小写

-- -----------------------------------------------------------------------------
--  外观
-- -----------------------------------------------------------------------------
opt.termguicolors = true   -- 启用 24bit 真彩色（需要终端支持）
opt.colorcolumn   = '80'   -- 在第 80 列画一条竖线，提示行长

-- -----------------------------------------------------------------------------
--  其他顺手的小细节
-- -----------------------------------------------------------------------------
opt.splitbelow   = true  -- 横分窗口默认往下
opt.splitright   = true  -- 竖分窗口默认往右
