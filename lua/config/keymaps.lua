-- =============================================================================
--  按键映射
-- =============================================================================

local keymap = vim.keymap

-- 默认快捷键前缀
-- 设置空格键作为 leader 键，所有 leader 快捷键都是先按空格
-- 例如: <leader>sv 实际上是按 [空格]sv
-- 你可以根据习惯修改，比如改成 ','
vim.g.mapleader = " "

-- -----------------------------------------------------------------------------
--  窗口操作
-- -----------------------------------------------------------------------------
-- <leader>sv: 垂直分割当前窗口 (创建左右并排的窗口)
-- <leader>sh: 水平分割当前窗口 (创建上下并排的窗口)
-- 这些是常用窗口布局操作，方便同时查看多个文件
keymap.set("n", "<leader>sv", "<C-w>v")
keymap.set("n", "<leader>sh", "<C-w>s")
-- 使用 Ctrl + hjkl 在窗口之间移动
keymap.set("n", "<C-h>", "<C-w>h")
keymap.set("n", "<C-j>", "<C-w>j")
keymap.set("n", "<C-k>", "<C-w>k")
keymap.set("n", "<C-l>", "<C-w>l")

-- -----------------------------------------------------------------------------
--  搜索高亮控制
-- -----------------------------------------------------------------------------
-- <leader>nh: 关闭当前搜索的高亮显示
-- 当你用 / 搜索文本后，所有匹配项会被高亮
-- 这个快捷键可以快速清除这些高亮，让界面更清爽
-- 再次搜索时高亮会自动重新出现
keymap.set("n", "<leader>nh", ":nohl<CR>")
